#include <unistd.h>

char	*ft_strlowcase(char *str)
{
	char	*p;

	p = str;
	while (*p)
	{
		if ('A' <= *p && *p <= 'Z')
		{
			*p += 32;
		}
		p++;
	}
	return (str);
}
